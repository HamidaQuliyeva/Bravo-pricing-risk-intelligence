CREATE OR REPLACE VIEW VW_EXECUTIVE_KPI AS
SELECT

(SELECT COUNT(*)
 FROM FACT_PRICING_RISK
 WHERE WEIGHTED_RISK_SCORE > 50) AS RISK_COUNT,

(SELECT COUNT(*)
 FROM FACT_PRICING_RISK
 WHERE PRICE_INDEX < 0.95) AS OPPORTUNITY_COUNT,

(SELECT ROUND(SUM(NO_REACTION_FLAG)/COUNT(*)*100,2)
 FROM FACT_COMPETITOR_REACTION) AS NO_REACTION_PCT,

(SELECT ROUND(SUM(DELAYED_FLAG)/COUNT(*)*100,2)
 FROM FACT_COMPETITOR_REACTION) AS DELAYED_REACTION_PCT,

(
SELECT SUM(MAX_RISK)
FROM (
    SELECT GLOBAL_PRODUCT_ID,
    MAX(COMMERCIAL_RISK_AZN) AS MAX_RISK
    FROM FACT_PRICING_RISK
    WHERE WEIGHTED_RISK_SCORE > 50
    GROUP BY GLOBAL_PRODUCT_ID
)
) AS REVENUE_AT_RISK_AZN

FROM DUAL;

select * from VW_EXECUTIVE_KPI;


CREATE OR REPLACE VIEW VW_CATEGORY_RISK AS

SELECT
DP.CATEGORY_GROUP AS CATEGORY_GROUP,

COUNT(DISTINCT FP.GLOBAL_PRODUCT_ID) AS SKU_COUNT,

ROUND(AVG(FP.PRICE_INDEX),3) AS AVG_PRICE_INDEX,

ROUND(AVG(FP.WEIGHTED_RISK_SCORE),2) AS CATEGORY_RISK_SCORE,

ROUND(SUM(MAX_RISK),2) AS EXPOSURE_AZN

FROM
(
    SELECT
        GLOBAL_PRODUCT_ID,
        MAX(COMMERCIAL_RISK_AZN) AS MAX_RISK,
        AVG(PRICE_INDEX) AS PRICE_INDEX,
        AVG(WEIGHTED_RISK_SCORE) AS WEIGHTED_RISK_SCORE
    FROM FACT_PRICING_RISK
    GROUP BY GLOBAL_PRODUCT_ID
) FP

JOIN DIM_PRODUCT DP
ON FP.GLOBAL_PRODUCT_ID = DP.GLOBAL_PRODUCT_ID

GROUP BY DP.CATEGORY_GROUP;

select * from VW_CATEGORY_RISK ;
commit;
