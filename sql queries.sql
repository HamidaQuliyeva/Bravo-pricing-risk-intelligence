CREATE OR REPLACE VIEW VW_MARKET_PRICE_POSITION AS
SELECT
CASE
WHEN PRICE_INDEX < 1 THEN 'Cheaper than market'
WHEN PRICE_INDEX = 1 THEN 'Price parity'
ELSE 'More expensive than market'
END AS PRICE_POSITION,
COUNT(*) AS SKU_COUNT,
ROUND(
COUNT(*) * 100 / SUM(COUNT(*)) OVER(),
2
) AS SHARE_PCT
FROM FACT_PRICING_RISK
GROUP BY
CASE
WHEN PRICE_INDEX < 1 THEN 'Cheaper than market'
WHEN PRICE_INDEX = 1 THEN 'Price parity'
ELSE 'More expensive than market'
END;

select * from VW_MARKET_PRICE_POSITION;
commit;


CREATE  VIEW VW_PRICING_DIAGNOSIS AS
SELECT DP.CATEGORY_GROUP AS CATEGORY,DP.PRODUCT,FP.PRICE_INDEX,FP.WEIGHTED_RISK_SCORE,FP.COMMERCIAL_RISK_AZN,FP.PROMO_PRESSURE,
    CASE
        WHEN FP.PROMO_PRESSURE = 'High Pressure'
            THEN 'Competitor promotion activity'

        WHEN FP.PROMO_PRESSURE = 'Market Wide Promo'
            THEN 'Market promotion pressure'

        WHEN FP.PRICE_INDEX > 1.05
            THEN 'Structural price gap'
        ELSE 'Minor deviation'
    END AS RISK_REASON
FROM FACT_PRICING_RISK FP JOIN DIM_PRODUCT DP ON FP.GLOBAL_PRODUCT_ID = DP.GLOBAL_PRODUCT_ID
WHERE FP.WEIGHTED_RISK_SCORE > 50;
select * from VW_PRICING_DIAGNOSIS;

commit;

